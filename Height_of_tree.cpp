#include <bits/stdc++.h>
#include <vector>
using namespace std;

struct Node
{
	int data;
	struct Node* left;
	struct Node* right;
	Node(int val)
	{
		data=val;
		left=right=NULL;
	}
};
int height_tree(Node* root)
{
	if(root==NULL)
		return 0;
	int lh=height_tree(root->left);
	int rh=height_tree(root->right);
	return 1+max(lh,rh);
}
